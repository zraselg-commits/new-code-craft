type Translations = typeof translations.en;

interface LanguageContextType {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: Translations;
}

const LanguageContext = createContext<LanguageContextType>({
  lang: "bn",
  setLang: () => {},
  t: translations.bn,
});

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  // Start with 'bn' so server HTML matches Bengali-first platform.
  // All public-facing text uses suppressHydrationWarning to handle
  // any edge cases where the stored preference differs.
  const [lang, setLangState] = useState<Lang>("bn");
  const [hydrated, setHydrated] = useState(false);

  useEffect(() => {
    // Mark hydration complete on the next tick (after React has committed
    // and all Suspense boundaries have resolved their client HTML).
    const timer = setTimeout(() => {
      setHydrated(true);

      // Step 1: restore stored preference
      const stored = localStorage.getItem("lang");
      if (stored === "bn" || stored === "en") {
        setLangState(stored as Lang);
        return;
      }
      // Step 2: country-code shortcut — default to 'bn' for Bangladesh
      const country = localStorage.getItem("rc_country");
      // No stored preference → default to Bengali (BD-first platform)
      if (country === "BD" || country === null) { setLangState("bn"); localStorage.setItem("lang", "bn"); return; }
      if (country !== null) return; // other country → keep "en"
      // Step 3: geo-detect (first visit, no stored data)
      import("@/lib/geoLocale").then(({ detectGeoLocale }) =>
        detectGeoLocale().then(({ lang: detected }) => {
          setLangState(detected as Lang);
          localStorage.setItem("lang", detected);
        })
      );
    }, 0);

    return () => clearTimeout(timer);
  }, []);

  const handleSetLang = (newLang: Lang) => {
    setLangState(newLang);
    localStorage.setItem("lang", newLang);
  };

  // During hydration, expose the stored lang (or bn default).
  // After hydration, always expose real lang.
  const effectiveLang: Lang = lang;

  return (
    <LanguageContext.Provider value={{ lang: effectiveLang, setLang: handleSetLang, t: translations[effectiveLang] }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => useContext(LanguageContext);
